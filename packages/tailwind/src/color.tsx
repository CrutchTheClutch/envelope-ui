function getColor(map: IColorMap, color: IColor, shade: IColorShade = "500") {
  switch (color) {
    case "none":
      return "";
    case "transparent":
    case "black":
    case "white":
      return map[color]["none"];

    default:
      return map[color][shade];
  }
}

